// Comentário de uma linha

/*
    Comentário de quebra de linhas
    
    classe.método(argumento) - representado pelo "cubo rosa"

    Write - Imprime na mesma línha sem pular
    WriteLine - Imprime a frase e pula de linha
*/

Console.Write("Hello, World! "); // A impressão da frase.
Console.Write("Estamos juntos novamente\n\n");
Console.WriteLine(10);
Console.WriteLine(2 + 10);
Console.WriteLine("Total = " + 2 + 10); // Concatenação: +
Console.WriteLine("Total = " + (3 + 10)); // Concatenação: +


